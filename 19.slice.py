# 切片,有了切片，很多地方不再需要循环了
l = list(range(100))

l1 = l[0:3] # 从索引0开始取，到索引3结束，包含0不包含3
l2 = l[:2] # 第一位为0时可以省略
l3 = l[-2:] # 倒数第二位截取到最后
l4 = l[-2:-1] # 倒数第二位截取到倒数第一位
l5 = l[:10:2] # 前10个数，每两个取一个
l6 = l[::5] # 所有数，每5个取一个
l7 = l[:] # 原样复制一个

print('l1', l1)
print('l2', l2)
print('l3', l3)
print('l4', l4)
print('l5', l5)
print('l6', l6)
print('l7', l7)

# tuple与字符串也可看成一种list，对应切片操作后的数据仍为tuple与字符串
print((1, 2, 3, 4)[:2])
